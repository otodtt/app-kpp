function run1() {
    var different = document.getElementsByClassName('type_pack1')[0].value;
    if (different == 1) {
        $( ".different_row1" ).removeClass( "hidden" );
        $( ".hide_number1" ).addClass( "hidden" );
    }
    else {
        $( ".different_row1" ).addClass( "hidden" );
        $( ".hide_number1" ).removeClass( "hidden" );
    }
}
